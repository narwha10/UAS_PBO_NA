    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        String judul = JudulCatatan.getText();
        String catatan = IsiCatatan.getText();

        try (Connection conn = koneksi.getKoneksi()) {
            String query = "UPDATE notes SET judul=?, catatan=? WHERE id=?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, judul);
            pstmt.setString(2, catatan);
            pstmt.setInt(3, id);
            pstmt.executeUpdate();
            menuUtama.loadData();
            this.dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saving note: " + e.getMessage());
        }
    }//GEN-LAST:event_SimpanActionPerformed

